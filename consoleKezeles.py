import pyconio  

def setErrorColor():
    pyconio.textcolor(pyconio.BLACK)
    pyconio.textbackground(pyconio.RED)

def setOutputColor():
    pyconio.textcolor(pyconio.BLACK)
    pyconio.textbackground(pyconio.BLUE)

def setConfirmColor():
    pyconio.textcolor(pyconio.BLACK)
    pyconio.textbackground(pyconio.GREEN)